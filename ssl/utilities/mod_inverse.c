__int128	mod_inverse(__int128 a, __int128 m)
{
	__int128 m0;
	__int128 y;
	__int128 x;
	__int128 q;
	__int128 t;

	m0 = m;
	y = 0;
	x = 1;
	if (m == 1)
		return (0);
	while (a > 1)
	{
		q = a / m;
		t = m;
		m = a % m;
		a = t;
		t = y;
		y = x - q * y;
		x = t;
	}
	if (x < 0)
		x += m0;
	return (x);
}
